#include <stdio.h>
/*
斐波那契数列又称生兔子问题：
有一对兔子，从出生后第3个月起每个月都生一对兔子，小兔子长到第三个月
后每个月又生一对兔子，假如兔子都不死，问每个月的兔子总数为多少？
兔子的规律为数列1,1,2,3,5,8,13,21....
编程打印前50项
扩展： 试着改变以下程序，打印出前后两项的商，你会看到它无限接近一个神秘的数字。
*/
int main10 ()
{
	double n1,n2,nn;
	int i;
	n1 = n2 = 1;
	printf("%.f,%.f,",n1,n2);
	for ( i = 3; i <= 50; i++)
	{ 
		nn = n1 + n2;
		printf("%.f,",nn);
		n1 = n2;
		n2 = nn;
		
	}
	return 0;
}