#include <stdio.h>
/*
鸡兔同笼： 鸡兔共35个头94只脚，
问：鸡兔各有多少只？
依题意：得到如下方程组：
	x+y=35
	2x+4y=94
*/
int main210 ()
{
	//-------方法（一）-------//
	int x,y;
	for (  x = 1; x <=35; x++)
	{
		for (  y = 1; y <=35-x; y++)
		{
			if (x+y==35 && 2*x+4*y==94)
			{
				printf("鸡有%i只  兔子有%i只\n",x,y);
			}
		}
	}
	//------方法（二）------//
	/*
	 解方程组：
	 x+y=35
	2x+4y=94
	
	x = 2*35-94/2
	y = 94/2-35
	
	*/
	x = 2*35-94/2;
	y = 94/2-35;
	printf("鸡有%i只  兔子有%i只\n",x,y);

	return 0;
} 